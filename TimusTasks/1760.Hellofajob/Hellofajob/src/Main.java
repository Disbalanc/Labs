import java.util.Scanner;

//1760. Адская работа
//Ограничение времени: 2.0 секунды
//Ограничение памяти: 64 МБ
//Железнодорожники Василий и Пётр попали в ад. В качестве первого наказания они должны были выполнить полный осмотр железной дороги Москва — Владивосток. Много недель они шли рядом, один вдоль левого рельса, другой — вдоль правого, и переписывали длинные заводские номера шпал в толстенные блокноты. Но как только они закончили это адское задание, то сразу получили новое, ещё более бессмысленное. Теперь они должны были сосчитать количество пар шпал, которые у Василия были записаны на одной странице блокнота, а у Петра на разных.
//Товарищи пришли к вам во сне и просят спасти их от этой страшной муки.
//Исходные данные
//Единственная строка содержит целые числа a, b, n (1 ≤ a, b ≤ n ≤ 25 000 000). На одну страницу блокнота у Василия помещается a номеров шпал, а у Петра — b номеров. Всего они переписали номера n шпал. Все эти номера различны и записаны в их блокнотах в одном и том же порядке.
//        Результат
//Выведите одно число — ответ задачи.

public class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int a = scanner.nextInt();
        int b = scanner.nextInt();
        int n = scanner.nextInt();

        long total = 0;
        for (int i = 0; ; i++) {
            long L = (long) i * a + 1;
            if (L > n) break;
            long R = Math.min((long) (i + 1) * a, n);
            long k = R - L + 1;
            if (k < 2) continue;

            long pairsInBlock = k * (k - 1) / 2;
            long start = L;
            while (start <= R) {
                long page = (start - 1) / b;
                long nextStart = page * b + b + 1;
                long end = Math.min(R, nextStart - 1);
                long count = end - start + 1;
                pairsInBlock -= count * (count - 1) / 2;
                start = nextStart;
            }
            total += pairsInBlock;
        }
        System.out.println(total);
    }
}